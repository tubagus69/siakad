    private void tblPasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPasienMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPasien.getModel();
        int row = tblPasien.getSelectedRow();
        
        txtNo.setText(model.getValueAt(row, 0).toString());
        txt_Norekam.setText(model.getValueAt(row, 1).toString());
        txtNama.setText(model.getValueAt(row, 2).toString());
        cmbJK.setSelectedItem(model.getValueAt(row, 3).toString());
        txtAlamat.setText(model.getValueAt(row, 4).toString());
        txtTtl.setText(model.getValueAt(row, 5).toString());

    }//GEN-LAST:event_tblPasienMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Pasien ang = new Pasien();
        ang.setNo(Integer.parseInt(txtNo.getText()));
        ang.setNo_rekam_medik(Integer.parseInt(txt_Norekam.getText()));
        ang.setNama(txtNama.getText());
        ang.setAlamat(txtAlamat.getText());
        ang.setJenis_kelamin(String.valueOf(cmbJK.getSelectedItem()));
        ang.setTgl_lahir(txtTtl.getText());
        
        ang.save();
        txtNo.setText(Integer.toString(ang.getNo()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPasien.getModel();
        int row = tblPasien.getSelectedRow();
        
        Pasien ang = new Pasien().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        ang.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

