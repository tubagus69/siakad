    public void tampilkanData(){
        String[] kolom = {"No", "No_rekam_medik", "Nama", "Alamat","Jenis_kelamin","Tanggal Lahir"};
        ArrayList<Pasien> list = new Pasien().getAll();
        Object rowData[] = new Object[6];
        
        tblPasien.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        for(Pasien ang : list){
            rowData[0] = ang.getNo();
            rowData[1] = ang.getNo_rekam_medik();
            rowData[2] = ang.getNama();
            rowData[3] = ang.getAlamat();
            rowData[4] = ang.getJenis_kelamin();
            rowData[5] = ang.getTgl_lahir();
            
            ((DefaultTableModel)tblPasien.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom = {"No", "No_rekam_medik", "Nama", "Alamat","Jenis_kelamin","Tanggal Lahir"};
        ArrayList<Pasien> list = new Pasien().search(keyword);
        Object rowData[] = new Object[6];
        
        tblPasien.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        for(Pasien ang : list){
            rowData[0] = ang.getNo();
            rowData[1] = ang.getNo_rekam_medik();
            rowData[2] = ang.getNama();
            rowData[3] = ang.getAlamat();
            rowData[4] = ang.getJenis_kelamin();
            rowData[5] = ang.getTgl_lahir();
            
            ((DefaultTableModel)tblPasien.getModel()).addRow(rowData);
        }
    }
